# Tuples

# Tuples are lists that you cannot update after initializing them (immutable)
# Empty tuples are defined by parenthesis ()

chars = ('a','b','c','d') # Parenthesis in tuple declaration are optional
chars2 = ()

# List methods (insert, pop, append, remove) dont work on tuples
# Tuples can access values from specified index
# Use only when you need a constant sequence of values that dont change